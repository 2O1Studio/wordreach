export const getInitialLetters = (setNumber) => {
  if (setNumber === "daily") {
    return getLetterSet(new Date().toLocaleDateString()).map(
      (letter, index) => ({
        id: `${letter}-${index}`,
        letter: letter,
        played: false,
      })
    );
  }
  return letterSets[setNumber].map((letter, index) => ({
    id: `${letter}-${index}`,
    letter: letter,
    played: false,
  }));
};
